  public void reloadImages() {
    if (mScrollView == null) {
      return;
    }
    if (mRecycleImageManager != null && mRecycleImageManager.isRecycleImage()) {
      WXSDKManager.getInstance().postOnUiThread(new Runnable() {
        @Override
        public void run() {
          if (mRecycleImageManager != null) {
            mRecycleImageManager.loadImage();
          }
        }
      }, 250);
    }
  }

